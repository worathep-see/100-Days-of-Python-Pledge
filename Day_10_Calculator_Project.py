def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1/ n2

operators = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide,
}
def calculator():
    should_calculate = True
    first_number = float(input("What's the first number?: "))

    while should_calculate:
        for symbol in operators:
            print(symbol)

        operator_option = input("Pick an operation: ")
        second_number = float(input("What's the second number?: "))
        answer = operators[operator_option](first_number, second_number)
        print(f"{first_number} {operator_option} {second_number} = {answer}")

        choice = input(f"Type 'y' to continue calculating with {answer}, or type 'n' to start a new calculation: ")
        if choice == "y":
            first_number = answer

        else:
            should_calculate = False
            print("\n" * 20)
            calculator()
            
calculator()
